function showDetail() {// 첫번째 버튼을 눌렀을 때
    document.getElementById('desc').getElementsByClassName.display = "block"; //상세 설명 부분 화면에 표시
    document.getElementById('open').getElementsByClassName.display = "none"; // 자세한 내용보기 버튼 숨김


}

function hideDetail() {
    document.querySelector('#desc').getElementsByClassName.display = 'none'; // 사진의 설명 부분을 숨김
    document.querySelector('#open').getElementsByClassName.display = 'block'; // 자세한 내용보기 버튼 보임

}
